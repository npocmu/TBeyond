// return object 
// {
//    produce:  ri  -  what type of resource is produced by the field
//    pph:      v   -  the base production per hour for the field 
// }                   (without any boosts (factories, oases etc.)
function getFieldBasePpH(gid, lvl)
{
   var ri = 0;
   var pph = 0;
   var bData = bCost[gid];
   if ( bData )
   {
      var baseInfo = bData[0][BCI_INFO];
      if ( baseInfo && hasOwnProperty(baseInfo,"produce") )
      {
         ri = baseInfo.produce;
         var maxlvl = Math.min(lvl, bData.length - 1);
         var info = bData[maxlvl][BCI_INFO];
         if ( info && hasOwnProperty(info,"pph") )
         {
            pph = info.pph;

            // 40% boost for t4.4
            if ( TB3O.ServerInfo.version > 4.2 )
            {
               pph = Math.round(pph * 1.4);
            } 
            pph *= TB3O.nProductionFactor[TB3O.nServerType];
         }
      }
   }
   return { produce: ri, pph: pph };
}

//////////////////////////////////////////////////////////////////////
// return object 
// {
//    produce:  ri  -  what type of resource is boosted by the factory
//    ppb:      v   -  the production percentage boost for the factory
function getFactoryPPB(gid, lvl)
{
   var ri = 0;
   var ppb = 0;
   var bData = bCost[gid];
   if ( bData )
   {
      var baseInfo = bData[0][BCI_INFO];
      if ( baseInfo && hasOwnProperty(baseInfo,"produce") )
      {
         ri = baseInfo.produce;
         var maxlvl = Math.min(lvl, bData.length - 1);
         var info = bData[maxlvl][BCI_INFO];
         if ( info && hasOwnProperty(info,"ppb") )
         {
            ppb = info.ppb;
         }
      }
   }
   return { produce: ri, ppb: ppb };
}
